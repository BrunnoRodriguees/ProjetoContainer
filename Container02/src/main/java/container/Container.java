package container;

public class Container {

	
	private int cliente;
	private int numContainer;
	private String tipo;
	private String statusContainer;
	private String categoria;
	public int getCliente() {
		return cliente;
	}
	public void setCliente(int cliente) {
		this.cliente = cliente;
	}
	public int getNumContainer() {
		return numContainer;
	}
	public void setNumContainer(int numContainer) {
		this.numContainer = numContainer;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	public String getStatusContainer() {
		return statusContainer;
	}
	public void setStatusContainer(String statusContainer) {
		this.statusContainer = statusContainer;
	}
	public String getCategoria() {
		return categoria;
	}
	public void setCategoria(String categoria) {
		this.categoria = categoria;
	}
	
	
}
